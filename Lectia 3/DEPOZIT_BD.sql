CREATE TABLE PRODUSE
(
	MODEL_ID INT PRIMARY KEY IDENTITY (1,1),
	PRODUCATOR CHAR(1),
	TIP VARCHAR(20),
	ACTIV BIT DEFAULT 1
)

CREATE TABLE PC
(
	PC_ID INT PRIMARY KEY IDENTITY (1,1),
	MODEL_ID INT FOREIGN KEY REFERENCES PRODUSE(MODEL_ID),
	VITEZA DECIMAL(4,0),
	RAM DECIMAL(4,0),
	HDD DECIMAL(4,0),
	CD VARCHAR(3),
	PRET FLOAT,
	ACTIV BIT DEFAULT 1
)

CREATE TABLE LAPTOPURI
(
	LAPTOP_ID INT PRIMARY KEY IDENTITY (1,1),
	MODEL_ID INT FOREIGN KEY REFERENCES PRODUSE(MODEL_ID),
	VITEZA DECIMAL(4,0),
	HDD DECIMAL(4,0),
	ECRAN INT,
	PRET FLOAT,
	ACTIV BIT DEFAULT 1
)

CREATE TABLE IMPRIMANTE 
(
	IMPRIMANTA_ID INT PRIMARY KEY IDENTITY (1,1),
	MODEL_ID INT FOREIGN KEY REFERENCES PRODUSE(MODEL_ID),
	COLOR VARCHAR(2),
	TIP VARCHAR(6),
	PRET FLOAT,
	ACTIV BIT DEFAULT 1
)

